use crate::parse::Variant;
use crate::rule::{Declaration, RuleMatch};

pub fn render_stylesheet(rules: &[RuleMatch]) -> String {
    let mut blocks = Vec::new();
    blocks.push(String::from("/* motif.css: generated by motif */"));

    for rule in rules {
        blocks.push(render_rule(rule));
    }

    blocks.join("\n\n") + "\n"
}

fn render_rule(rule: &RuleMatch) -> String {
    let selector = render_selector(&rule.raw_class_name);
    let body = render_declarations(&rule.declarations);

    let mut pseudo_suffix = String::new();
    let mut is_dark = false;
    for variant in &rule.variants {
        match variant {
            Variant::Hover => pseudo_suffix.push_str(":hover"),
            Variant::Focus => pseudo_suffix.push_str(":focus"),
            Variant::Active => pseudo_suffix.push_str(":active"),
            Variant::Dark => is_dark = true,
        }
    }

    let block = format!("{selector}{pseudo_suffix} {{\n{body}\n}}", selector = selector, pseudo_suffix = pseudo_suffix, body = body);
    if is_dark {
        format!("@media (prefers-color-scheme: dark) {{\n{}}}\n", indent(&block, 2).trim_end())
    } else {
        block
    }
}

fn render_selector(raw_class_name: &str) -> String {
    format!(".{}", escape_class_name(raw_class_name))
}

fn escape_class_name(input: &str) -> String {
    let mut escaped = String::new();
    for ch in input.chars() {
        match ch {
            ':' | '/' | '.' => {
                escaped.push('\\');
                escaped.push(ch);
            }
            _ => escaped.push(ch),
        }
    }
    escaped
}

fn render_declarations(declarations: &[Declaration]) -> String {
    declarations
        .iter()
        .map(|declaration| format!("  {}: {};", declaration.property, declaration.value))
        .collect::<Vec<_>>()
        .join("\n")
}

fn indent(input: &str, spaces: usize) -> String {
    let prefix = " ".repeat(spaces);
    input
        .lines()
        .map(|line| format!("{prefix}{line}"))
        .collect::<Vec<_>>()
        .join("\n")
}

#[cfg(test)]
mod tests {
    use super::render_stylesheet;
    use crate::parse::parse_class_name;
    use crate::rule::resolve_rule;

    #[test]
    fn renders_plain_and_variant_rules() {
        let plain = resolve_rule(&parse_class_name("f-stack").unwrap()).unwrap();
        let hover = resolve_rule(&parse_class_name("hover:f-bg-primary").unwrap()).unwrap();

        let stylesheet = render_stylesheet(&[plain, hover]);

        assert!(stylesheet.contains(".f-stack {"));
        assert!(stylesheet.contains("display: flex;"));
        assert!(stylesheet.contains(".hover\\:f-bg-primary:hover {"));
        assert!(stylesheet.contains("background-color: #0f6cbd;"));
    }

    #[test]
    fn wraps_dark_variant_in_media_query() {
        let dark = resolve_rule(&parse_class_name("dark:m-elevation-1").unwrap()).unwrap();
        let stylesheet = render_stylesheet(&[dark]);

        assert!(stylesheet.contains("@media (prefers-color-scheme: dark) {"));
        assert!(stylesheet.contains(".dark\\:m-elevation-1 {"));
        assert!(stylesheet.contains("box-shadow: 0 1px 2px rgba(0, 0, 0, 0.3);"));
    }
}
